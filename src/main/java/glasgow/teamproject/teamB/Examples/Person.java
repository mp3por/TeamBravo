package glasgow.teamproject.teamB.Examples;


public class Person {
	private Integer age;
	private String name;
	private Integer id;
	
	public Integer getAge() {
		return age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public void setAge(Integer age) {
		this.age = age;
	}
	
	
}
